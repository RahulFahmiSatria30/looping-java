package j08;
import java.util.Scanner;
public class contoh01 {
    public static void main(String[] args)
    {
        int x;
        for(x = 1; x <= 5; x++)
        {
            System.out.println("SMK TELKOM MALANG");
        }
        
        //PERULANGAN WHILE
        int i = 1;
        while(i <= 5)
        {
            System.out.println("SMK TELKOM MALANG");
            i++;
        }
        
        //PERULANGAN DO WHILE
        int j =1;
        do
        {
            System.out.println("SMK TELKOM MALANG");
            j++;
        }
        while(j <= 5);
    }
    
}
